            initComponents();
            setLocationRelativeTo(null);
            getContentPane().setBackground(Color.white);
            btnStart.setEnabled(true);
            btnStop.setEnabled(false);
            Properties properties = new Properties();
            File file = new File("settings/ConnectionProp.properties");
            FileReader inputStream = new FileReader(file.getAbsolutePath());
            properties.load(inputStream);
            registry = LocateRegistry.createRegistry(Integer.parseInt(properties.getProperty("port")));
            skeleton = ServiceFactoryImpl.getInstance();
            txtStatus.setText("Server is ready to start");
